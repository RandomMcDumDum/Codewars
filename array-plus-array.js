// My Solution
// const arrayPlusArray = (arr1, arr2) =>  arr1.reduce((accu, curr) => accu + curr,0)+ arr2.reduce((accu, curr) => accu + curr,0)

// Better solution

const arrayPlusArray = (arr1, arr2) => [...arr1, ...arr2].reduce((accu, curr) => accu + curr, 0) 


// Tests
console.log(arrayPlusArray([1, 2, 3], [4, 5, 6])) // Expected Output: 21
console.log(arrayPlusArray([-1, -2, -3], [-4, -5, -6])) // Expected Output: -21
console.log(arrayPlusArray([0, 0, 0], [4, 5, 6])) // Expected Output: 15
console.log(arrayPlusArray([100, 200, 300], [400, 500, 600])) // Expected Output: 2100



































































































































































